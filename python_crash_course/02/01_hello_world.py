# 2.2.1 变量的命名和使用
# 变量名只能包含字母/数字和下划线.变量名只能以字母或下划线打头,但不能以数字打头.
# 变量名不能包含空格,但能使用下划线来分隔其中的字母.
# 不要将 Python 关键字和函数名用作变量名.
# 变量名应即剪短又具有描述性.
# 慎用小谢字母l和大写字母O,因为他们可能被人看错成数字1 和 0 .
